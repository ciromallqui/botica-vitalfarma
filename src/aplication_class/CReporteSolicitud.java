package aplication_class;
public class CReporteSolicitud {
    private String fechaVenta;
    private String turno;
    private String presentacion;
    private String producto;
    private int mes;
    private String fechaVentaInicial;
    private String fechaVentaFinal;
    private String origen;

    public String getFechaVenta() {
        return fechaVenta;
    }

    public void setFechaVenta(String fechaVenta) {
        this.fechaVenta = fechaVenta;
    }

    public String getTurno() {
        return turno;
    }

    public void setTurno(String turno) {
        this.turno = turno;
    }

    public String getPresentacion() {
        return presentacion;
    }

    public void setPresentacion(String presentacion) {
        this.presentacion = presentacion;
    }

    public String getProducto() {
        return producto;
    }

    public void setProducto(String producto) {
        this.producto = producto;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public String getFechaVentaInicial() {
        return fechaVentaInicial;
    }

    public void setFechaVentaInicial(String fechaVentaInicial) {
        this.fechaVentaInicial = fechaVentaInicial;
    }

    public String getFechaVentaFinal() {
        return fechaVentaFinal;
    }

    public void setFechaVentaFinal(String fechaVentaFinal) {
        this.fechaVentaFinal = fechaVentaFinal;
    }

    public String getOrigen() {
        return origen;
    }

    public void setOrigen(String origen) {
        this.origen = origen;
    }
    
}
